function exp_admin_block_status(edata, trial_data)

    block_status = grpstats(trial_data, 'block', {'nanmean', 'min', 'numel'}, 'DataVars', {'trial_num', 'rt', 'complete'});

    block_status = dataset_reorg(block_status, ...
        'block', '', ...
        'min_trial_num', 'trial_start', ...
        'numel_complete', 'trial_count', ...
        'nanmean_rt', 'rt', ...
        'nanmean_complete', 'complete' ...
    );

    fprintf('Block Status:\n');
    disp(block_status);
    
end